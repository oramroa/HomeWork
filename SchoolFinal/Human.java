package SchoolFinal;

public class Human {
     String fullName;    // ФИО полностью
     String surname;     // фамилия
     String name;        // имя
     String patronymic;  // отчество

     public String getFullName() {
          return fullName;
     }

     public String getSurname() {
          return surname;
     }

     public String getName() {
          return name;
     }

     public String getPatronymic() {
          return patronymic;
     }
}
